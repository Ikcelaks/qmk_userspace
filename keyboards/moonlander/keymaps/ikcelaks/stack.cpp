#include "stack.h"
#include <stdio.h>

//////////////////////////////////////////////////////////////////////////////////
void stack_push(stack_t *s, char c)
{
	if (s->size < STACK_SIZE)
		s->buffer[s->size++] = c;
}
//////////////////////////////////////////////////////////////////////////////////
void stack_pop(stack_t *s)
{
	if (s->size > 0)
		s->size--;
}
//////////////////////////////////////////////////////////////////////////////////
void stack_print(stack_t *s)
{
	for (int i = s->size - 1; i >= 0; --i)
		printf("%c", s->buffer[i]);
}